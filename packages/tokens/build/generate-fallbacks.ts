import { writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { primitiveColor } from "../src/primitive/color";

export interface FallbackValues {
  borderDefault: string;
  textPrimary: string;
  textInverse: string;
  bgSurface: string;
}

export function buildFallbacks(): FallbackValues {
  return {
    borderDefault: primitiveColor.stone[200],
    textPrimary: primitiveColor.stone[900],
    textInverse: primitiveColor.stone[50],
    bgSurface: primitiveColor.white,
  };
}

function emit(fb: FallbackValues): string {
  return `/* AUTO-GENERATED by build/generate-fallbacks.ts — do not edit by hand. */
export const FALLBACKS = ${JSON.stringify(fb, null, 2)} as const;

export type FallbackKey = keyof typeof FALLBACKS;
`;
}

async function main() {
  const __dirname = dirname(fileURLToPath(import.meta.url));
  const out = resolve(__dirname, "../src/fallbacks.ts");
  await writeFile(out, emit(buildFallbacks()));
  console.log("✅ Generated fallbacks:", out);
}

if (import.meta.url === `file://${process.argv[1]}` || process.argv[1]?.endsWith("generate-fallbacks.ts")) {
  main().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}
